
public class Studenti {
	int id;
	String ime;
	int godini;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIme() {
		return ime;
	}
	public void setIme(String ime) {
		this.ime = ime;
	}
	public int getGodini() {
		return godini;
	}
	public void setGodini(int godini) {
		
		this.godini = godini;
	}
	
}
